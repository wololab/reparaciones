                        <p>
                            La principal causa de la rotura del parabrisas del automóvil es el impacto de
                            gravilla proyectada hacia el parabrisas, representando un 80% de los casos.
                            Existen principalmente 4 tipos de roturas en el parabrisas: desgaste superficial,
                            ojo de buey, ojo de buey con micro-fisuras, y rotura con grietas.
                        </p>
                        <p>
                            El desgaste superficial supone una ligera marca en el parabrisas, la cual no corre ningún
                            riesgo de degenerar.
                        </p>
                        <p>
                            El ojo de buey es un agujero cónico de unos 1-2,5 cm de diámetro de
                            base que se asienta en la cara exterior del parabrisas, el vértice de este cono se
                            sitúa en la lámina de PVB. En el caso del ojo de buey, raramente hay agrietamiento
                            posterior del cristal.
                        </p>
                        <p>
                            El ojo de buey con micro-fisuras es un tipo de rotura que aúna un ojo de buey con grietas
                            como consecuencia del impacto. Estas grietas normalmente tienen forma de estrella y el
                            riesgo de que se propaguen es elevado.
                        </p>
                        <p>
                            Finalmente, la rotura con grietas es la más peligrosa para el parabrisas porque es la que
                            presenta una mayor probabilidad de propagarse ante una solicitación (fuerza) externa.
